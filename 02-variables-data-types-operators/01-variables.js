// Biến với let
// Khai báo biến diemSo bằng let và gán giá trị ban đầu là 8.
let diemSo = 8;

// Có thể gán lại giá trị cho biến được khai báo bằng let.
diemSo = 9;

// In giá trị hiện tại của diemSo ra màn hình.
console.log(diemSo);

// Hằng số với const
// Khai báo hằng số ngaySinh và gán giá trị chuỗi ngày sinh.
const ngaySinh = "14/02/1993";

// In giá trị của ngaySinh ra màn hình.
console.log(ngaySinh);

// const không thể được gán lại giá trị sau khi đã khai báo.

